    /**
     * Handle the transaction "updated" event.
     *
     * @param  \App\Transaction  $transaction
     * @return void
     */
    public function updated(Transaction $transaction)
    {
        if ($transaction->isDirty('remain')){
            if ($transaction->remain <= 0){
                $transaction->complete();
            }
        }
    }

